    private void checkSospensione() {
        String stato = selectStato();
        if(stato.equals("E")) {
            sospendiButton.setEnabled(true);
        }
        else
            sospendiButton.setEnabled(false);
    }
    private void checkEmissione() {
        String stato = selectStato();
        if(stato.equals("NE")) {
            cancellaButton.setEnabled(true);
        }
        else
            cancellaButton.setEnabled(false);
    }
    private String selectStato() {
        int index = ingiunzioniTable.getSelectedRow();
        TableModel model = ingiunzioniTable.getModel();
        return model.getValueAt(index, 3).toString();
    }
    private String selectIngiunzione() {
        int index = ingiunzioniTable.getSelectedRow();
        TableModel model = ingiunzioniTable.getModel();
        return model.getValueAt(index, 0).toString();
    }
    
     private void checkIfEnableRicerca() {
         if(nomeTextField.getText().length()<=0 && cognomeTextField.getText().length()<=0 &&idUtenteTextField.getText().length()<=0 && indirizzoTextField.getText().length()<=0 && cittaTextField.getText().length()<=0)
            ricercaButton.setEnabled(false);
        else
            ricercaButton.setEnabled(true);
    }
    
    
