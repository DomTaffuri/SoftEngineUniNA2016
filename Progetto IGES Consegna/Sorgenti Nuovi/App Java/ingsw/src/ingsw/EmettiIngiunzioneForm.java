    public void setIngiunzioni(ResultSet rst) {
      DefaultTableModel dtm = new DefaultTableModel(); 
      try {
       ResultSetMetaData meta = rst.getMetaData();
       int columns = meta.getColumnCount();
       for (int i = 1; i <= columns; i++){
            dtm.addColumn(meta.getColumnName(i));
        }
       
        Object [] rowData = new Object[columns];
       
        while(rst.next()) {  
                for (int i = 0; i < columns; i++)
                {
                    rowData[i] = rst.getObject(i+1);
                }
            dtm.addRow(rowData);  
                   
        }
      }
      catch(SQLException e){
          JOptionPane.showMessageDialog(this, "Non Ã¨ stato possibile caricare la tabella","Errore", JOptionPane.ERROR_MESSAGE);
      }
       ingiunzioniTable.setModel(dtm);  
    }
    
    public void resetIngiunzioni() {
        ResultSet ingiunzioni = controller.resetIngiunzioni();
        setIngiunzioni(ingiunzioni);
    }
